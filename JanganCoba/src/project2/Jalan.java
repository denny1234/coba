package project2;

class Jalan {
    int awal, akhir;

    public int getAwal() {
        return awal;
    }

    public int getAkhir() {
        return akhir;
    }

    public String getNama() {
        return nama;
    }

    public Jalan(int awal, int akhir, String nama) {
        this.awal = awal;
        this.akhir = akhir;
        this.nama = nama;
    }
    String nama;
    
}
