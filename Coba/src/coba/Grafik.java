    private double konversiLonkeX(double lon) {
        double x = (180+lon) * (mapwidth / 360)*10000;
        return x;
        
    }

    private double konversiLatkeY(double lat) {
        double y = (90-lat) * (mapheight / 180)*10000;
        return y;
    }
