    public void paint(Graphics g){
        Graphics2D g2 = (Graphics2D) g;
        tampil(g2);
        tampilGaris(g2);
    }
    
    private void tampil(Graphics2D g2){
        Titik A;
        double x,y;
        for (int i = 0; i < kumpulanTitik.size(); i++) {
            A = kumpulanTitik.get(i);
            //System.out.println(A.getX()+","+A.getY()+","+A.getLabel()+","+A.getNomor());
            x = konversiLonkeX(A.getX());
            y = konversiLatkeY(A.getY());
            g2.draw(new Ellipse2D.Double((int) x,(int) y,5,5));
            //g2.drawString(A.getLabel(),A.getX(),A.getY());
        }
    }
    
    private void tampilGaris(Graphics2D g2){
        Garis A;
        Titik Awal, Akhir;
        int x1, y1, x2, y2;
        for (int i = 0; i < kumpulanGaris.size(); i++) {
            A = kumpulanGaris.get(i);
            //System.out.println(A.getAwal()+","+A.getAkhir()+","+A.getLabel()+","+A.getJarak());
            Awal = kumpulanTitik.get(A.getAwal()-1);
            Akhir = kumpulanTitik.get(A.getAkhir()-1);
            x1 = Awal.getX();
            y1 = Awal.getY();
            x2 = Akhir.getX();
            y2 = Akhir.getY();
            g2.draw(new Line2D.Double(x1, y1, x2, y2));
            
    }
    }

